/// Провайдер для управления языком приложения
final localeProvider = StateNotifierProvider<LocaleNotifier, Locale>((ref) {
  return LocaleNotifier();
});

class LocaleNotifier extends StateNotifier<Locale> {
  LocaleNotifier() : super(const Locale('en'));

  /// Переключает между русским и английским языками
  void toggleLanguage() {
    state = state.languageCode == 'en'
        ? const Locale('ru')
        : const Locale('en');
  }

  /// Устанавливает английский язык
  void setEnglish() {
    state = const Locale('en');
  }

  /// Устанавливает русский язык
  void setRussian() {
    state = const Locale('ru');
  }

  /// Устанавливает язык по коду ('en' | 'ru')
  void setLocale(String languageCode) {
    state = languageCode == 'ru' ? const Locale('ru') : const Locale('en');
  }

  /// Проверяет, является ли текущий язык русским
  bool get isRussian => state.languageCode == 'ru';

  /// Проверяет, является ли текущий язык английским
  bool get isEnglish => state.languageCode == 'en';
}

