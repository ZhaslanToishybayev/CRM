  /// Получает перевод для текущего языка
  static String translate(Locale locale, String key) {
    final lang = locale.languageCode;
    final translations = _translations[lang] ?? _translations['en']!;
    return translations[key] ?? key;
  }

  /// Расширение для Locale для удобного доступа к переводам
  static String of(BuildContext context, String key) {
    final locale = Localizations.localeOf(context);
    return translate(locale, key);
  }
}

/// Хук для использования локализации в виджетах
extension LocalizationExtension on BuildContext {
  String t(String key) {
    return LocalizationExtension._localeOf(this, key);
  }

  static String _localeOf(BuildContext context, String key) {
    final locale = Localizations.localeOf(context);
    return LocalizationService.translate(locale, key);
  }
}
