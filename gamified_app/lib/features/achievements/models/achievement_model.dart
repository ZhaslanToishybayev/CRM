/// Achievement Model
/// Educational Project - Gamified Task Management App
///
/// Represents an achievement badge and its properties

import 'package:freezed_annotation/freezed_annotation.dart';

part 'achievement_model.freezed.dart';
part 'achievement_model.g.dart';

/// Achievement categories
enum AchievementCategory {
  streaks,
  completions,
  speed,
  special,
  levels,
  xp,
  tasks,
  time,
}

/// Achievement rarity
enum AchievementRarity {
  common,
  rare,
  epic,
  legendary,
  mythic,
}

@freezed
class AchievementModel with _$AchievementModel {
  const AchievementModel._(); // Added for getters

  const factory AchievementModel({
    required String id,
    required String name,
    required String description,
    required AchievementCategory category,
    required AchievementRarity rarity,
    required String icon,
    required String color,
    required String condition,
    required int targetValue,
    required int xpReward,
    String? hint,
    @Default(false) bool isHidden,
  }) = _AchievementModel;

  factory AchievementModel.fromJson(Map<String, dynamic> json) =>
      _$AchievementModelFromJson(json);

  /// Get rarity display name
  String get rarityDisplayName {
    switch (rarity) {
      case AchievementRarity.common:
        return 'Common';
      case AchievementRarity.rare:
        return 'Rare';
      case AchievementRarity.epic:
        return 'Epic';
      case AchievementRarity.legendary:
        return 'Legendary';
      case AchievementRarity.mythic:
        return 'Mythic';
    }
  }

  /// Get category display name
  String get categoryDisplayName {
    switch (category) {
      case AchievementCategory.streaks:
        return 'Streaks';
      case AchievementCategory.completions:
        return 'Completions';
      case AchievementCategory.speed:
        return 'Speed';
      case AchievementCategory.special:
        return 'Special';
      case AchievementCategory.levels:
        return 'Levels';
      case AchievementCategory.xp:
        return 'XP';
      case AchievementCategory.tasks:
        return 'Tasks';
      case AchievementCategory.time:
        return 'Time';
    }
  }

  /// Get rarity color
  String get rarityColor {
    switch (rarity) {
      case AchievementRarity.common:
        return '#9E9E9E';
      case AchievementRarity.rare:
        return '#2196F3';
      case AchievementRarity.epic:
        return '#9C27B0';
      case AchievementRarity.legendary:
        return '#FF9800';
      case AchievementRarity.mythic:
        return '#FFD700';
    }
  }
}

