  /// Get all work achievements
  static List<AchievementModel> get allWork => all;

  /// Get all team achievements
  static List<AchievementModel> get allTeam => teamAchievements;

  /// Get all work and team achievements combined
  static List<AchievementModel> get allCombined => [...all, ...teamAchievements];

  /// Get achievements by work category
  static List<AchievementModel> getByCategory(WorkAchievementCategory category) {
    // Map work categories to base categories
    final baseCategory = _mapWorkCategoryToBase(category);
    return all.where((a) => a.category == baseCategory).toList();
  }

  /// Get achievements by rarity
  static List<AchievementModel> getByRarity(AchievementRarity rarity) {
    return allCombined.where((a) => a.rarity == rarity).toList();
  }

  /// Get achievement by ID
  static AchievementModel? getById(String id) {
    try {
      return allCombined.firstWhere((a) => a.id == id);
    } catch (e) {
      return null;
    }
  }

  /// Get total count of work achievements
  static int get workCount => all.length;

